CREATE OR ALTER TRIGGER BI_ETHNICGROUP__ID FOR ETHNICGROUP
BEFORE INSERT
AS
BEGIN
  IF (NEW.ID IS NULL) THEN
    NEW.ID = NEXT VALUE FOR SQ_ETHNICGROUP__ID;
END^

