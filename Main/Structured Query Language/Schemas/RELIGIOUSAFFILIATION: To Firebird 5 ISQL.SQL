CREATE OR ALTER TRIGGER BI_RELIGIOUSAFFILIATION__ID FOR RELIGIOUSAFFILIATION
BEFORE INSERT
AS
BEGIN
  IF (NEW.ID IS NULL) THEN
    NEW.ID = NEXT VALUE FOR SQ_RELIGIOUSAFFILIATION__ID;
END^

