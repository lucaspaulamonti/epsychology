COMMENT ON COLUMN TBBLOOD.NAME IS 'ACRONYM THAT COMBINES BLOOD TYPE WITH AGLUTININ AND RH FACTOR.';
COMMENT ON COLUMN TBBLOOD.OBSERVATION IS 'OBSERVATION FIELD FOR ADDITIONAL INFORMATION ABOUT BLOOD TYPE.';

COMMIT WORK;

