/*
TBHOLIDAY: TABLE FOR STORING INFORMATION ABOUT HOLIDAYS
*/

CREATE TABLE TBHOLIDAY (
  	ID_ SERIAL NOT NULL PRIMARY KEY,
	NAME VARCHAR(45) NOT NULL UNIQUE,
  	DDMM CHAR(4) NOT NULL UNIQUE,
	BUSINESSDAY CHAR(1) NOT NULL,
  	OBSERVATION VARCHAR(255)
);

COMMIT WORK;

COMMENT ON TABLE TBHOLIDAY IS '';
COMMENT ON COLUMN TBHOLIDAY.ID_ IS '';
COMMENT ON COLUMN TBHOLIDAY.NAME IS '';
COMMENT ON COLUMN TBHOLIDAY.DDMM IS '';
COMMENT ON COLUMN TBHOLIDAY.BUSINESSDAY IS 'IF "S" PEOPLE WORK, IF "N" PEOPLE DOESNT WORK';
COMMENT ON COLUMN TBHOLIDAY.OBSERVATION IS '';

COMMIT WORK;

