COMMENT ON COLUMN TBCUSTOMER.IDMARITAL_ IS '';
COMMENT ON COLUMN TBCUSTOMER.IDRELIGION_ IS '';
COMMENT ON COLUMN TBCUSTOMER.IDBLOOD_ IS '';
COMMENT ON COLUMN TBCUSTOMER.IDZOODIAC_ IS '';
COMMENT ON COLUMN TBCUSTOMER.IDETHNICITY_ IS '';
COMMENT ON COLUMN TBCUSTOMER.IDOCCUPATION_ IS '';

