COMMENT ON COLUMN TBGENDEREXPRESSION.ID_ IS 'UNIQUE IDENTIFIER FOR THE GENDER EXPRESSION CATEGORY.';
COMMENT ON COLUMN TBGENDEREXPRESSION.NAME IS 'NAME OF THE GENDER EXPRESSION CATEGORY.';
COMMENT ON COLUMN TBGENDEREXPRESSION.OBSERVATION IS 'ADDITIONAL OBSERVATIONS OR COMMENTS REGARDING THE GENDER EXPRESSION CATEGORY.';

COMMIT WORK;

