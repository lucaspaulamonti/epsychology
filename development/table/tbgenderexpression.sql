/*
TABLE TBGENDEREXPRESSION: TABLE TO STORE GENDER EXPRESSION CATEGORIES
*/

CREATE TABLE TBGENDEREXPRESSION (
    	ID_ SERIAL NOT NULL PRIMARY KEY,
    	NAME VARCHAR(45) NOT NULL UNIQUE,
	OBSERVATION VARCHAR(255)
);

COMMIT WORK;

