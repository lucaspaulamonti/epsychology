/*
    TBMODEL: TABLE CONTAINING INFORMATION ABOUT DOCUMENT MODELS
*/

CREATE TABLE TBMODEL(
    ID_ SERIAL NOT NULL PRIMARY KEY,
    NAME VARCHAR(75) NOT NULL UNIQUE,
    PATH VARCHAR(255) NOT NULL UNIQUE,

    IDCOMPANY_ INTEGER NOT NULL,

    OBSERVATION VARCHAR(255),

    FOREIGN KEY (IDCOMPANY_) REFERENCES TBCOMPANY (ID_)
);

COMMIT WORK;

COMMENT ON TABLE TBMODEL IS '';
COMMENT ON COLUMN TBMODEL.ID_ IS '';
COMMENT ON COLUMN TBMODEL.NAME IS '';
COMMENT ON COLUMN TBMODEL.PATH IS '';
COMMENT ON COLUMN TBMODEL.IDCOMPANY_ IS '';
COMMENT ON COLUMN TBMODEL.OBSERVATION IS '';

