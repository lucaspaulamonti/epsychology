	COMPLETED/REVIEWED 27/08/2024
*/

CREATE TABLE TBUSER (
    ID_ SERIAL NOT NULL PRIMARY KEY,
    IDCOLLABORATOR_ INTEGER NOT NULL UNIQUE

    NAME VARCHAR(45) NOT NULL UNIQUE,
    PASS VARCHAR(25) NOT NULL,

    DATE_CREATION DATE DEFAULT CURRENT_DATE,
    DATE_EXCLUSION DATE
);

COMMIT WORK;

COMMENT ON TABLE TBUSER IS 'TABLE TO STORE USERS INFORMATION';

COMMENT ON COLUMN TBUSER.ID_ IS 'UNIQUE IDENTIFIER FOR THE USER';
COMMENT ON COLUMN TBUSER.IDCOLLABORATOR_ IS 'REFERENCE TO THE COLLABORATOR ASSOCIATED WITH THE USER';
COMMENT ON COLUMN TBUSER.NAME IS 'USERNAME FOR THE USER';
COMMENT ON COLUMN TBUSER.PASS IS 'PASSWORD FOR THE USER ACCOUNT';
COMMENT ON COLUMN TBUSER.DATE_CREATION IS 'DATE WHEN THE USER ACCOUNT WAS CREATED';
COMMENT ON COLUMN TBUSER.DATE_EXCLUSION IS 'DATE WHEN THE USER ACCOUNT WAS EXCLUDED OR DEACTIVATED';

COMMIT WORK;