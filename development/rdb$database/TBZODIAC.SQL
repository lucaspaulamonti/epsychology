COMMENT ON COLUMN TBZODIAC.NAME IS 'ZODIAC SIGN NAME.';
COMMENT ON COLUMN TBZODIAC.OBSERVATION IS 'FIELD FOR ADDITIONAL OBSERVATIONS ABOUT THE ZODIAC SIGN.';

COMMIT WORK;

