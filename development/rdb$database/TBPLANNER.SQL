/*
	TBPLANNER: TABLE TO STORE INFORMATION ABOUT
*/

CREATE TABLE TBPLANNER (
	ID_ SERIAL NOT NULL PRIMARY KEY,
    
	IDWEEK_ INTEGER,
	IDTIME_ INTEGER,
	IDROOM_ INTEGER,

	IDCUSTOMER_ INTEGER,
	IDCOLABORATOR_ INTEGER,
	IDSERVICE_ INTEGER,
	
	IDCOMPANY_ INTEGER,

	CONFIRMEE CHAR(1),
	OBSERVATION VARCHAR(255),

	FOREIGN KEY (IDWEEK_) REFERENCES TBWEEK (ID_),
	FOREIGN KEY (IDTIME_) REFERENCES TBTIME (ID_),
	FOREIGN KEY (IDROOM_) REFERENCES TBROOM (ID_),

	FOREIGN KEY (IDCUSTOMER_) REFERENCES TBCUSTOMER (ID_),
	FOREIGN KEY (IDCOLABORATOR_) REFERENCES TBCOLABORATOR (ID_),
	FOREIGN KEY (IDSERVICE_) REFERENCES TBSERVICE (ID_),

	FOREIGN KEY (IDCOMPANY_) REFERENCES TBCOMPANY (ID_)
);

COMMIT WORK;

COMMENT ON TABLE TBPLANNER IS '';

COMMENT ON COLUMN TBPLANNER.ID_ IS '';

COMMENT ON COLUMN TBPLANNER.IDWEEK_ IS '';
COMMENT ON COLUMN TBPLANNER.IDTIME_ IS '';
COMMENT ON COLUMN TBPLANNER.IDROOM_ IS '';

COMMENT ON COLUMN TBPLANNER.IDCUSTOMER_ IS '';
COMMENT ON COLUMN TBPLANNER.IDCOLABORATOR_ IS '';
COMMENT ON COLUMN TBPLANNER.IDSERVICE_ IS '';

COMMENT ON COLUMN TBPLANNER.IDCOMPANY_ IS '';

COMMENT ON COLUMN TBPLANNER.CONFIRMEE IS '';
COMMENT ON COLUMN TBPLANNER.OBSERVATION IS '';

COMMIT WORK;